
public class Operadores {

	public static void main(String[] args) {
		int numero1 = 10;
		int numero2 = 20;
		
		//soma
		System.out.println(numero1 + numero2);
		
		//subtração
		System.out.println(numero2 - numero1);
		
		//multiplicação
		System.out.println(numero1 * numero2);

		//divisao
		System.out.println(numero2 / numero2);
	}

}
